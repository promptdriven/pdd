// Example 4: Full workflow — List → Generate → Verify
// ============================================================================

/**
 * Demonstrates the typical end-to-end workflow:
 *   1. GET /api/pipeline/compositions/list to discover what's missing
 *   2. POST /api/pipeline/compositions/run to generate missing components
 *   3. GET /api/pipeline/compositions/list again to verify results
 *
 * This pattern is used by the pipeline editor UI to drive the compositions stage.
 */

async function example4_fullWorkflow(): Promise<void> {
  console.log("=== Example 4: Full Workflow (List → Generate → Verify) ===\n");

  console.log(`
  // Step 1: Discover what needs to be generated
  const listResponse = await fetch('/api/pipeline/compositions/list');
  const { sections } = await listResponse.json();

  // Collect missing and errored components across all sections
  const toGenerate: string[] = [];
  const wrappersToGenerate: string[] = [];

  for (const section of sections) {
    for (const comp of section.components) {
      if (comp.status === 'missing' || comp.status === 'error') {
        toGenerate.push(comp.name);
      }
    }
    for (const wrapper of section.wrappers) {
      if (wrapper.status === 'missing') {
        wrappersToGenerate.push(wrapper.name);
      }
    }
  }

  if (toGenerate.length === 0 && wrappersToGenerate.length === 0) {
    console.log('All compositions are up to date!');
    return;
  }

  // Step 2: Generate missing components via SSE stream
  console.log(\\\`Generating \\\${toGenerate.length} components and \\\${wrappersToGenerate.length} wrappers...\\\\\`);

  const runResponse = await fetch('/api/pipeline/compositions/run', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      components: toGenerate,
      wrappers: wrappersToGenerate,
    }),
  });

  // Consume SSE stream and track results
  const results = new Map<string, string>();
  const reader = runResponse.body!.getReader();
  const decoder = new TextDecoder();
  let buffer = '';

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;

    buffer += decoder.decode(value, { stream: true });
    const lines = buffer.split('\\\\n\\\\n');
    buffer = lines.pop() ?? '';

    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const event = JSON.parse(line.slice(6));

      if (event.type === 'component') {
        results.set(event.name, event.status);
        console.log(\\\`  \\\${event.name}: \\\${event.status}\\\\\`);
      }
    }
  }

  // Step 3: Verify by listing again
  const verifyResponse = await fetch('/api/pipeline/compositions/list');
  const verified = await verifyResponse.json();

  let allDone = true;
  for (const section of verified.sections) {
    for (const comp of [...section.components, ...section.wrappers]) {
      if (comp.status !== 'exists') {
        allDone = false;
        console.log(\\\`  Still not ready: \\\${comp.name} (\\\${comp.status})\\\\\`);
      }
    }
  }

  if (allDone) {
    console.log('All compositions generated successfully!');
  } else {
    console.log('Some compositions still need attention.');
  }
  `);

  console.log("");
}

