// example_usage.ts
// Comprehensive usage example for the composition generation pipeline API routes.
//
// This module provides three API endpoints for a video production pipeline:
//   1. POST /api/pipeline/compositions/run — Generates Remotion components via Claude Code
//   2. POST /api/pipeline/asset-staging/run — Copies Veo output assets to Remotion public dir
//   3. GET /api/pipeline/compositions/list — Lists all compositions with their status
//
// NOTE: These are Next.js Route Handlers (server-only). This example demonstrates
// how to call them from a client, how the SSE streaming works, and how the internal
// helpers function.

// ============================================================================
// Example 1: Calling POST /api/pipeline/compositions/run (SSE client)
// ============================================================================

/**
 * The POST /api/pipeline/compositions/run endpoint accepts a JSON body:
 *
 * @param body.components - string[] — Names of Remotion components to generate.
 *   Each name corresponds to a spec file in the `specs/` directory.
 *   Claude Code is invoked once per component with Read+Write+Edit+Glob tools
 *   scoped to `remotion/src/remotion/`.
 *
 * @param body.wrappers - string[] — Names of section wrapper compositions to generate.
 *   These are generated by running `python3 generate_section_compositions.py`.
 *
 * @returns An SSE (Server-Sent Events) stream with the following event types:
 *   - { type: 'component', name: string, status: 'generating' | 'done' | 'error' }
 *     Emitted per-component as generation progresses.
 *   - { type: 'job', jobId: string }
 *     Emitted once the pipeline stage job is created.
 *   - { type: 'complete', jobId: string }
 *     Emitted when all components and wrappers have been processed.
 *   - { type: 'error', message: string }
 *     Emitted if the pipeline stage fails.
 *   - { type: 'log', message: string, jobId: string }
 *     Emitted for each log line from Claude Code or the wrapper script.
 *   - { type: 'progress', percent: number, jobId: string }
 *     Emitted as components complete (percentage of total).
 */

async function example1_compositionsRun(): Promise<void> {
  console.log("=== Example 1: POST /api/pipeline/compositions/run ===\n");

  const requestBody = {
    components: ["IntroTitle", "BackgroundGradient", "LogoReveal"],
    wrappers: ["introWrapper", "IntroCompositionWrapper"],
  };

  console.log("Request body:", JSON.stringify(requestBody, null, 2));

  // In a real app, you'd fetch from the actual endpoint:
  //
  // const response = await fetch('/api/pipeline/compositions/run', {
  //   method: 'POST',
  //   headers: { 'Content-Type': 'application/json' },
  //   body: JSON.stringify(requestBody),
  // });
  //
  // The response is an SSE stream. Here's how to consume it:

  const sseConsumerCode = `
  const response = await fetch('/api/pipeline/compositions/run', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      components: ['IntroTitle', 'BackgroundGradient', 'LogoReveal'],
      wrappers: ['introWrapper'],
    }),
  });

  const reader = response.body!.getReader();
  const decoder = new TextDecoder();
  let buffer = '';

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;

    buffer += decoder.decode(value, { stream: true });
    const lines = buffer.split('\\\\n\\\\n');
    buffer = lines.pop() ?? '';

    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const event = JSON.parse(line.slice(6));

      switch (event.type) {
        case 'component':
          console.log(\\\`Component \\\${event.name}: \\\${event.status}\\\\\`);
          // 'generating' → Claude Code is working on it
          // 'done'       → Component file created successfully
          // 'error'      → Generation failed
          break;

        case 'progress':
          console.log(\\\`Progress: \\\${event.percent}%\\\\\`);
          break;

        case 'log':
          console.log(\\\`Log: \\\${event.message}\\\\\`);
          break;

        case 'job':
          console.log(\\\`Job created: \\\${event.jobId}\\\\\`);
          break;

        case 'complete':
          console.log(\\\`Pipeline complete. Job ID: \\\${event.jobId}\\\\\`);
          break;

        case 'error':
          console.error(\\\`Pipeline error: \\\${event.message}\\\\\`);
          break;
      }
    }
  }
  `;

  console.log("SSE consumer pattern (client-side):");
  console.log(sseConsumerCode);

  // Simulate the SSE events that would be emitted:
  const simulatedEvents = [
    { type: "component", name: "IntroTitle", status: "generating" },
    { type: "log", message: "[compositions] Generating component: IntroTitle", jobId: "job-abc-123" },
    { type: "component", name: "IntroTitle", status: "done" },
    { type: "progress", percent: 20, jobId: "job-abc-123" },
    { type: "component", name: "BackgroundGradient", status: "generating" },
    { type: "component", name: "BackgroundGradient", status: "done" },
    { type: "progress", percent: 40, jobId: "job-abc-123" },
    { type: "component", name: "LogoReveal", status: "generating" },
    { type: "component", name: "LogoReveal", status: "done" },
    { type: "progress", percent: 60, jobId: "job-abc-123" },
    { type: "component", name: "introWrapper", status: "generating" },
    { type: "component", name: "IntroCompositionWrapper", status: "generating" },
    { type: "component", name: "introWrapper", status: "done" },
    { type: "component", name: "IntroCompositionWrapper", status: "done" },
    { type: "progress", percent: 100, jobId: "job-abc-123" },
    { type: "job", jobId: "job-abc-123" },
    { type: "complete", jobId: "job-abc-123" },
  ];

  console.log("Simulated SSE event sequence:");
  for (const event of simulatedEvents) {
    console.log(`  data: ${JSON.stringify(event)}`);
  }
  console.log("");
}

