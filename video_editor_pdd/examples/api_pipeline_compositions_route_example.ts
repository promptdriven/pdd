/**
 * The createSseStream() helper creates a TransformStream-based SSE writer.
 *
 * SSE format:
 *   Each event is a line starting with "data: " followed by a JSON object,
 *   terminated by two newlines (\n\n).
 *
 * The stream emits these event types in order:
 *
 * For each component in the `components` array:
 *   1. { type: 'component', name: '<name>', status: 'generating' }
 *   2. { type: 'log', message: '[compositions] Generating component: <name>', jobId: '<id>' }
 *   3. (Multiple log events from Claude Code stderr)
 *   4. { type: 'component', name: '<name>', status: 'done' }  — or 'error'
 *   5. { type: 'progress', percent: <N>, jobId: '<id>' }
 *
 * For wrappers (all at once via python script):
 *   1. { type: 'component', name: '<name>', status: 'generating' } — for each wrapper
 *   2. (Log events from python script stdout/stderr)
 *   3. { type: 'component', name: '<name>', status: 'done' | 'error' } — for each wrapper
 *   4. { type: 'progress', percent: <N>, jobId: '<id>' }
 *
 * Finally:
 *   - { type: 'job', jobId: '<id>' }
 *   - { type: 'complete', jobId: '<id>' }
 *
 * On error:
 *   - { type: 'error', message: '<error message>' }
 *   - Stream closes
 */

