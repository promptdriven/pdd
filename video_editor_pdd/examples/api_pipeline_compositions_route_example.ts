// Example 10: React hook pattern for consuming the SSE stream
// ============================================================================

/**
 * Demonstrates a React hook pattern for consuming the compositions SSE stream
 * in a Next.js client component.
 */

function example10_reactHookPattern(): void {
  console.log("=== Example 10: React Hook Pattern ===\n");

  console.log(`
  // hooks/useCompositionGeneration.ts
  'use client';

  import { useState, useCallback } from 'react';

  type ComponentStatus = {
    name: string;
    status: 'pending' | 'generating' | 'done' | 'error';
  };

  export function useCompositionGeneration() {
    const [components, setComponents] = useState<ComponentStatus[]>([]);
    const [progress, setProgress] = useState(0);
    const [jobId, setJobId] = useState<string | null>(null);
    const [isRunning, setIsRunning] = useState(false);
    const [error, setError] = useState<string | null>(null);

    const generate = useCallback(async (
      componentNames: string[],
      wrapperNames: string[]
    ) => {
      setIsRunning(true);
      setError(null);
      setProgress(0);

      // Initialize component statuses
      setComponents([
        ...componentNames.map(name => ({ name, status: 'pending' as const })),
        ...wrapperNames.map(name => ({ name, status: 'pending' as const })),
      ]);

      const response = await fetch('/api/pipeline/compositions/run', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          components: componentNames,
          wrappers: wrapperNames,
        }),
      });

      const reader = response.body!.getReader();
      const decoder = new TextDecoder();
      let buffer = '';

      try {
        while (true) {
          const { done, value } = await reader.read();
          if (done) break;

          buffer += decoder.decode(value, { stream: true });
          const lines = buffer.split('\\\\n\\\\n');
          buffer = lines.pop() ?? '';

          for (const line of lines) {
            if (!line.startsWith('data: ')) continue;
            const event = JSON.parse(line.slice(6));

            switch (event.type) {
              case 'component':
                setComponents(prev =>
                  prev.map(c =>
                    c.name === event.name
                      ? { ...c, status: event.status }
                      : c
                  )
                );
                break;
              case 'progress':
                setProgress(event.percent);
                break;
              case 'job':
                setJobId(event.jobId);
                break;
              case 'error':
                setError(event.message);
                break;
            }
          }
        }
      } finally {
        setIsRunning(false);
      }
    }, []);

    return { components, progress, jobId, isRunning, error, generate };
  }

  // Usage in a component:
  // const { components, progress, isRunning, generate } = useCompositionGeneration();
  // <button onClick={() => generate(['IntroTitle'], ['introWrapper'])}>Generate</button>
  // <ProgressBar value={progress} />
  // {components.map(c => <ComponentStatus key={c.name} {...c} />)}
  `);
}

