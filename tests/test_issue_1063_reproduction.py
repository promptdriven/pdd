"""Regression tests for issue #1063.

Bug: ``checkup``/auto-heal can patch generated PDD code without updating the
source prompt, producing prompt/code drift in a bot-authored fix PR.

Background — PR promptdriven/pdd#1055
=====================================

The bot commit ``bf57242d9a18072f3d444f91d9a834ba067f15ca`` changed *only*
``pdd/agentic_update.py``.  Before the commit the prompt source
``pdd/prompts/agentic_update_python.prompt`` specified

    _compute_include_allowlist(prompt_path: Path, *, max_depth: int = 3) -> set[Path]
    ...
    cap recursion at max_depth levels

The bot rewrote the generated code to drop ``max_depth`` and traverse without a
depth cap, but did not touch the prompt.  For ~14 minutes the PR HEAD therefore
contained code that contradicted its prompt contract — exactly the drift the
issue describes.  A follow-up bot commit (``3106e382``) eventually patched the
prompt by hand, but nothing in the checkup/auto-heal pipeline *forced* that
follow-up.  The next ``pdd sync agentic_update`` could have overwritten the
direct code edit.

Acceptance criteria from the issue:

* "Add a regression where checkup fixer modifies a generated module whose prompt
  is present and registered in ``architecture.json``."
* "The workflow either updates the prompt and runs sync, or fails with a clear
  message requiring prompt-source changes."
* "A final drift check catches prompt/code contract divergence before marking
  checkup/auto-heal successful."

These tests assert the *expected* behaviour.  They are expected to FAIL on the
current (buggy) code because no such drift guard exists, and to PASS once the
fix lands.
"""

from __future__ import annotations

import json
import subprocess
from pathlib import Path
from typing import Tuple
from unittest.mock import patch

import pytest


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


PROMPT_WITH_MAX_DEPTH = """\
<pdd-reason>Test prompt for issue #1063 regression.</pdd-reason>

<pdd-interface>
{
  "type": "module",
  "module": {
    "functions": [
      {"name": "run_thing", "signature": "(x: int) -> int", "returns": "int"}
    ]
  }
}
</pdd-interface>

% Goal
Implement a helper ``_compute_include_allowlist(prompt_path: Path, *, max_depth: int = 3) -> set[Path]``
that performs a depth-bounded BFS over ``<include>`` references.  The helper
MUST cap recursion at ``max_depth`` levels.
"""


CODE_MATCHING_PROMPT = '''\
"""Generated by PDD from the test prompt."""

from pathlib import Path
from typing import Set


def run_thing(x: int) -> int:
    return x + 1


def _compute_include_allowlist(prompt_path: Path, *, max_depth: int = 3) -> Set[Path]:
    """Depth-bounded, cycle-safe BFS over <include> references."""
    # Real impl elided for the regression test.
    return set()
'''


CODE_DRIFTED_FROM_PROMPT = '''\
"""Generated by PDD from the test prompt — mutated by a hypothetical fixer."""

from pathlib import Path
from typing import Set


def run_thing(x: int) -> int:
    return x + 1


def _compute_include_allowlist(prompt_path: Path) -> Set[Path]:
    """Cycle-safe BFS over <include> references (no depth cap)."""
    return set()
'''


ARCH_ENTRY = {
    "filename": "mymod_python.prompt",
    "filepath": "pdd/mymod.py",
    "language": "Python",
    "reason": "Test module for issue #1063.",
    "description": "Toy module used to reproduce prompt/code drift.",
}


def _init_repo(root: Path) -> None:
    """Initialise a tiny git repo with the prompt/code/architecture layout."""
    (root / "pdd").mkdir(parents=True, exist_ok=True)
    (root / "pdd" / "prompts").mkdir(parents=True, exist_ok=True)

    (root / "pdd" / "prompts" / "mymod_python.prompt").write_text(
        PROMPT_WITH_MAX_DEPTH, encoding="utf-8"
    )
    (root / "pdd" / "mymod.py").write_text(CODE_MATCHING_PROMPT, encoding="utf-8")
    (root / "architecture.json").write_text(
        json.dumps([ARCH_ENTRY], indent=2), encoding="utf-8"
    )

    subprocess.run(["git", "init", "-q"], cwd=root, check=True)
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t", "add", "-A"],
        cwd=root,
        check=True,
    )
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t", "commit", "-q", "-m", "init"],
        cwd=root,
        check=True,
    )


def _simulate_fixer_code_only_edit(root: Path) -> Tuple[Path, Path]:
    """Mirror the bf57242 scenario: fixer rewrites generated code, leaves prompt alone."""
    code_path = root / "pdd" / "mymod.py"
    prompt_path = root / "pdd" / "prompts" / "mymod_python.prompt"
    code_path.write_text(CODE_DRIFTED_FROM_PROMPT, encoding="utf-8")
    return prompt_path, code_path


def _pr_metadata() -> dict:
    """Stable PR metadata used by `_commit_and_push_if_changed` tests."""
    return {
        "clone_url": "https://example.invalid/repo.git",
        "head_ref": "feature/test",
        "head_owner": "owner",
        "head_repo": "repo",
    }


