def test_prompt_drift_guard_module_detects_code_only_edit(tmp_path: Path):
    """The standalone ``pdd.prompt_drift_guard`` module must flag drift.

    Per Step 6, the guard helper lives in its own module so it can be reused
    independently of the commit/push path.  This test imports that module
    directly and asserts behaviour on a worktree mirroring the bf57242
    scenario: a prompt-backed module's code has changed without a matching
    prompt change.

    Expected to FAIL on current code (module does not exist yet) and PASS
    once ``pdd/prompt_drift_guard.py`` is added with the documented helper.
    """
    # Import lazily so pytest's collection error message is clear.
    try:
        import pdd.prompt_drift_guard as guard_mod
    except ImportError as exc:  # pragma: no cover - failure path
        pytest.fail(
            "pdd.prompt_drift_guard module must exist as a standalone callee "
            f"(see issue #1063 and Step 6 plan). Import failed: {exc!r}"
        )

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)
    _simulate_fixer_code_only_edit(repo)

    # The exact public symbol name is locked down here.  Any of the names
    # discussed in the Step 6 plan satisfies the contract — pick the first
    # one the module actually exposes.
    candidates = (
        "detect_prompt_code_drift",
        "verify_no_prompt_code_drift",
        "check_prompt_code_drift",
        "find_prompt_code_drift",
    )
    guard = None
    for name in candidates:
        if hasattr(guard_mod, name):
            guard = getattr(guard_mod, name)
            break
    assert guard is not None, (
        "pdd.prompt_drift_guard must expose a public helper. "
        f"Tried {candidates!r}. See issue #1063."
    )

    result = guard(repo)

    # The helper may return either a boolean drift-flag (False == no drift)
    # or a collection of offending paths/module names.  Both shapes are
    # acceptable; either way the result must clearly indicate drift on this
    # worktree.
    if isinstance(result, bool):
        assert result is True, (
            "Drift helper returned False (no drift) for a worktree that "
            "modified pdd/mymod.py without updating its prompt."
        )
    else:
        offenders = list(result) if result is not None else []
        assert offenders, (
            "Drift helper returned an empty/None result for a worktree that "
            "modified pdd/mymod.py without updating its prompt."
        )
        joined = " ".join(str(o) for o in offenders)
        assert "mymod" in joined, (
            "Drift helper did not flag pdd/mymod.py as drifting. "
            f"Reported offenders: {offenders!r}"
        )
