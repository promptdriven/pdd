def test_commit_and_push_allows_paired_prompt_and_code_edit(tmp_path: Path):
    """Fix that updates BOTH the prompt and the generated code must succeed.

    False-positive ward: the drift guard added for issue #1063 must not refuse
    the *correct* fix shape (prompt-source change + regenerated code).
    """
    from pdd.checkup_review_loop import _commit_and_push_if_changed

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)

    # Bot updated prompt AND regenerated code in the same commit (the
    # acceptance-criteria-compliant fix shape).
    (repo / "pdd" / "prompts" / "mymod_python.prompt").write_text(
        PROMPT_WITH_MAX_DEPTH.replace("max_depth: int = 3", "max_depth: int = 5"),
        encoding="utf-8",
    )
    (repo / "pdd" / "mymod.py").write_text(
        CODE_MATCHING_PROMPT.replace("max_depth: int = 3", "max_depth: int = 5"),
        encoding="utf-8",
    )

    with patch(
        "pdd.checkup_review_loop.push_with_retry",
        return_value=(True, ""),
    ) as mock_push:
        ok, message = _commit_and_push_if_changed(
            repo, _pr_metadata(), "fix: bump max_depth in prompt + regenerated code"
        )

    assert ok, (
        "Paired prompt+code edit must NOT be refused by the drift guard. "
        f"Got failure: {message!r}"
    )
    # The caller must actually invoke the push helper — proves we reached the
    # push site rather than short-circuiting silently.
    mock_push.assert_called_once()


def test_commit_and_push_allows_edit_to_non_prompt_backed_file(tmp_path: Path):
    """Edits to files NOT registered in architecture.json must pass the guard.

    No false-positives on documentation, CI config, tests, or other non-PDD
    artifacts.  This guards against an over-eager drift check that would
    refuse every commit.
    """
    from pdd.checkup_review_loop import _commit_and_push_if_changed

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)

    # Modify a tracked file that's not the prompt-backed module.  README is
    # not in architecture.json so the drift guard must ignore it.
    (repo / "README.md").write_text("# Project\n\nUpdated docs.\n", encoding="utf-8")
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t", "add", "README.md"],
        cwd=repo,
        check=True,
    )
    subprocess.run(
        ["git", "-c", "user.email=t@t", "-c", "user.name=t",
         "commit", "-q", "-m", "seed README"],
        cwd=repo,
        check=True,
    )
    # Now actually mutate it so there's a diff to commit.
    (repo / "README.md").write_text("# Project\n\nUpdated docs again.\n", encoding="utf-8")

    with patch(
        "pdd.checkup_review_loop.push_with_retry",
        return_value=(True, ""),
    ) as mock_push:
        ok, message = _commit_and_push_if_changed(
            repo, _pr_metadata(), "docs: update README"
        )

    assert ok, (
        "Edit to a non-prompt-backed file (README.md) must not be refused. "
        f"Got failure: {message!r}"
    )
    mock_push.assert_called_once()


def test_commit_and_push_refuses_newly_added_prompt_backed_code(tmp_path: Path):
    """Drift guard must catch NEW files too, not only modifications.

    A fixer that *adds* a generated module without authoring the corresponding
    prompt would otherwise slip through a diff-based guard.  The acceptance
    criteria explicitly demand consistency between prompt source and generated
    code regardless of whether the code is new or modified.
    """
    from pdd.checkup_review_loop import _commit_and_push_if_changed

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)

    # Register a SECOND prompt-backed module in architecture.json but only
    # create the generated code file — no prompt source.  This is exactly
    # the drift the guard must catch on newly added paths.
    arch_path = repo / "architecture.json"
    entries = json.loads(arch_path.read_text(encoding="utf-8"))
    entries.append(
        {
            "filename": "newmod_python.prompt",
            "filepath": "pdd/newmod.py",
            "language": "Python",
            "reason": "Newly added module for issue #1063 regression.",
            "description": "Brand-new prompt-backed module with no prompt source.",
        }
    )
    arch_path.write_text(json.dumps(entries, indent=2), encoding="utf-8")
    (repo / "pdd" / "newmod.py").write_text(
        '"""Newly authored generated code without a prompt source."""\n\n'
        "def foo() -> int:\n    return 1\n",
        encoding="utf-8",
    )

    with patch(
        "pdd.checkup_review_loop.push_with_retry",
        return_value=(True, ""),
    ) as mock_push:
        ok, message = _commit_and_push_if_changed(
            repo, _pr_metadata(), "feat: add newmod (forgot the prompt)"
        )

    assert not ok, (
        "Adding a prompt-backed module without authoring its prompt must be "
        f"refused. Got success with message: {message!r}"
    )
    assert "prompt" in message.lower(), (
        "Failure message must call out the missing prompt source. "
        f"Got: {message!r}"
    )
    # If the guard refused before push, push_with_retry must not have run.
    mock_push.assert_not_called()


def test_commit_and_push_ignores_only_pdd_meta_artifact_changes(tmp_path: Path):
    """Drift guard must not trip on PDD's own meta artifacts.

    ``.pdd/meta/*.json`` files are intentionally filtered out of the staging
    step (see ``_is_untracked_pdd_meta_artifact``).  If the *only* change in
    the worktree is such a meta artifact, the commit/push helper must return
    cleanly without raising a drift error — there is no prompt-backed code
    change to validate.
    """
    from pdd.checkup_review_loop import _commit_and_push_if_changed

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)

    # Only change is an untracked PDD meta artifact.
    meta_dir = repo / ".pdd" / "meta"
    meta_dir.mkdir(parents=True, exist_ok=True)
    (meta_dir / "scratch.json").write_text(
        json.dumps({"scratch": True}), encoding="utf-8"
    )

    with patch(
        "pdd.checkup_review_loop.push_with_retry",
        return_value=(True, ""),
    ) as mock_push:
        ok, message = _commit_and_push_if_changed(
            repo, _pr_metadata(), "chore: scratch meta artifact"
        )

    # Either no eligible changes (nothing to stage after filter) or a successful
    # push — both are acceptable.  What must NOT happen is a drift-style refusal.
    assert ok, (
        "Meta-artifact-only worktree must not be refused by the drift guard. "
        f"Got failure: {message!r}"
    )
    assert "prompt" not in message.lower(), (
        "No prompt should be mentioned when the only change is a PDD meta "
        f"artifact. Got: {message!r}"
    )
    # Should not have actually pushed anything since the meta file is filtered.
    mock_push.assert_not_called()


