def test_commit_and_push_refuses_code_only_edit_when_prompt_is_registered(tmp_path: Path):
    """Bot fixer commits that touch a prompt-backed module without touching the
    matching prompt MUST be refused.

    This mirrors the bf57242 scenario from PR #1055: only ``pdd/mymod.py``
    changes, while ``pdd/prompts/mymod_python.prompt`` (registered in
    ``architecture.json``) remains untouched.  The current
    ``_commit_and_push_if_changed`` flow has no awareness of
    ``architecture.json`` and will happily push the code-only commit.
    """
    from pdd.checkup_review_loop import _commit_and_push_if_changed

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)
    _simulate_fixer_code_only_edit(repo)

    # Mock the push helper so the test does not hit the network.  If the drift
    # guard works correctly, ``push_with_retry`` must never be called for this
    # worktree — we'll assert that below.
    with patch(
        "pdd.checkup_review_loop.push_with_retry",
        return_value=(True, ""),
    ) as mock_push:
        ok, message = _commit_and_push_if_changed(
            repo, _pr_metadata(), "fix: drift-introducing change"
        )

    assert not ok, (
        "Code-only edit to a prompt-backed module must be refused by "
        "_commit_and_push_if_changed. Got success with message: %r" % message
    )
    assert "prompt" in message.lower(), (
        "Failure message must explain that the prompt source needs updating. "
        f"Got: {message!r}"
    )
    # Drift must be refused before the push helper runs — otherwise the bot
    # would still write the drifted commit to the PR branch.
    mock_push.assert_not_called()


def test_drift_guard_flags_prompt_backed_module_with_only_code_changes(tmp_path: Path):
    """A standalone drift check should report the offending modules.

    Independent of how the guard is wired into the commit/push flow, the
    project should expose a pure function that, given a worktree, returns the
    set of prompt-backed modules whose generated code changed without a
    corresponding prompt change.  This locks down the public contract the
    acceptance criteria require.
    """
    import pdd.checkup_review_loop as crl

    repo = tmp_path / "repo"
    repo.mkdir()
    _init_repo(repo)
    _simulate_fixer_code_only_edit(repo)

    # Resolve the guard regardless of which exact name the fix introduces.
    guard = None
    for name in (
        "verify_no_prompt_code_drift",
        "detect_prompt_code_drift",
        "check_prompt_code_drift",
    ):
        if hasattr(crl, name):
            guard = getattr(crl, name)
            break
    if guard is None:
        pytest.fail(
            "No prompt/code drift guard is exported from "
            "pdd.checkup_review_loop. See issue #1063."
        )

    result = guard(repo)  # type: ignore[misc]

    # The guard may return either a boolean (False == drift detected) or a
    # collection of offending paths/modules.  Either form must indicate drift.
    if isinstance(result, bool):
        assert result is False, (
            "Drift guard returned True (no drift) for a worktree that "
            "modified pdd/mymod.py without updating its prompt."
        )
    else:
        offenders = list(result) if result is not None else []
        assert offenders, (
            "Drift guard returned an empty/None result for a worktree that "
            "modified pdd/mymod.py without updating its prompt."
        )
        joined = " ".join(str(o) for o in offenders)
        assert "mymod" in joined, (
            "Drift guard did not flag pdd/mymod.py as drifting. "
            f"Reported offenders: {offenders!r}"
        )


