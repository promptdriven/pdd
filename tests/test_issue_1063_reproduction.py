#
# Per Step 6, the drift-guard helper lives in a new module
# ``pdd.prompt_drift_guard`` and is re-exported from ``pdd.checkup_review_loop``.
# The other tests in this file exercise the caller side (via
# ``_commit_and_push_if_changed`` or via ``pdd.checkup_review_loop``'s re-export).
# This test exercises the callee module *directly* — i.e. it imports
# ``pdd.prompt_drift_guard`` and calls its public helper.  Without this, the
# fix could land in checkup_review_loop alone and silently sidestep the
# planned module split.
