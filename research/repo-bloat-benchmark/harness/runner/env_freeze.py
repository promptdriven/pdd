"""Run-environment freeze (design.md §8.1.1).

Filesystem isolation alone is not enough: the *agent* environment is a second
source of uncontrolled variance and hidden inputs — a resumed session, a warm
cache, a stray web search, or a developer config file can let the agent
"localize" without reading the repo. This module makes each run's agent
environment **frozen, isolated, and fingerprinted**:

- **Pinned CLI version** — captured by running the pinned binary's version
  command; any mismatch with the registered pin aborts before model tokens
  are spent.
- **Fresh, empty per-run ``CODEX_HOME``** — created by the harness, populated
  only with a generated config file, and **destroyed after the run** (session
  artifacts are archived to the run's report dir first). Ephemerality is
  guaranteed by deletion, not by trusting a CLI flag.
- **Generated config** — model id, reasoning effort, web-search setting, MCP
  set (empty unless the arm defines it), history persistence off, and the
  recording proxy as the model provider. The rendered bytes are covered by
  the fingerprint. Exact key names must be validated against the pinned
  Codex build before real runs (design §10 still-to-confirm); the fingerprint
  makes any adjustment visible.
- **Sanitized environment** — an allowlist of variable *names* is copied from
  the parent environment; everything else is dropped. ``HOME`` is redirected
  to the per-run home so ambient user config is not visible. The single
  permitted secret is the model API key, passed through by explicit name.
- **Egress guard** — proxy environment variables point all HTTP(S) traffic at
  an unroutable discard address, with ``NO_PROXY`` exempting only loopback
  (where the recording proxy listens). This is the *portable* layer: it is
  honored by mainstream HTTP stacks but is not kernel enforcement — the
  Linux-container network lockdown remains the hard tier, and session-log
  reconciliation (harness.context_snapshots.session_parser) detects bypass.
- **Fingerprint** — sha256 over the canonical frozen combination (CLI
  version, model, effort, web search, MCP set, allowlist names and values,
  config template with the per-run proxy port and auth env key *held as
  placeholders*, cache policy). Identical across cells and trials by
  construction; recorded as ``env_fingerprint_sha256`` in every run record,
  and asserted against the registered value before each run.
"""

from __future__ import annotations

import hashlib
import json
import os
import shutil
import subprocess
from dataclasses import dataclass, field
from pathlib import Path
from urllib.parse import urlparse

# Variable NAMES copied from the parent environment. Everything else is
# dropped — no inherited dev shell (design §8.1.1 "Shell environment").
DEFAULT_ENV_ALLOWLIST = ("PATH", "LANG", "LC_ALL", "TERM", "TMPDIR")

# All non-loopback HTTP(S) traffic is pointed here: TCP port 9 ("discard"),
# nothing listens. Requests fail fast instead of leaving the machine.
_BLACKHOLE_PROXY = "http://127.0.0.1:9"

_CONFIG_HEADER = (
    "# Generated by harness/runner/env_freeze.py — frozen run environment "
    "(design §8.1.1).\n"
    "# Key names validated against codex-cli 0.142.4 with --strict-config "
    "(see CODEX_PIN.md);\n"
    "# the env fingerprint covers this file, so any adjustment is a new "
    "registered environment.\n"
)


def _sha256_hex(data: bytes) -> str:
    """Non-security digest used for reproducibility fingerprints only."""
    try:
        return hashlib.sha256(data, usedforsecurity=False).hexdigest()
    except TypeError:
        return hashlib.sha256(data).hexdigest()


class FreezeViolation(RuntimeError):
    """A frozen-environment precondition failed; the run must not proceed."""


@dataclass
class FreezeConfig:
    """The registered (pre-run) frozen combination for an arm."""

    pinned_cli_version: str  # exact expected output of the version command
    cli_version_command: list[str]  # e.g. ["codex", "--version"]
    model_id: str
    reasoning_effort: str | None = None
    # With a custom model_provider the pinned build has no metadata for any
    # model id and falls back to defaults, so the context window that drives
    # client-side compaction MUST be registered explicitly for real runs.
    context_window_tokens: int | None = None
    web_search_enabled: bool = False
    mcp_servers: tuple[str, ...] = ()  # empty unless the arm definition says otherwise
    env_allowlist: tuple[str, ...] = DEFAULT_ENV_ALLOWLIST
    api_key_env_var: str | None = None  # the single permitted secret
    cache_policy: str = "disabled"
    home_dir_env_var: str = "CODEX_HOME"

    def render_config(
        self, proxy_base_url: str, *, auth_env_var_override: str | None = None
    ) -> str:
        """Render the per-run CLI config (TOML).

        Key names and values are validated against the pinned build
        (codex-cli 0.142.4) via ``--strict-config`` (see CODEX_PIN.md):
        ``wire_api = "chat"`` is rejected outright — the pinned build only
        speaks the Responses API — and only the top-level string
        ``web_search = "disabled"`` removes the ``web_search`` tool from the
        request's tool list (``[tools] web_search`` is inert for presence).
        """
        lines = [_CONFIG_HEADER]
        lines.append(f'model = "{self.model_id}"')
        if self.reasoning_effort is not None:
            lines.append(f'model_reasoning_effort = "{self.reasoning_effort}"')
        if self.context_window_tokens is not None:
            lines.append(f"model_context_window = {self.context_window_tokens}")
        if not self.web_search_enabled:
            lines.append('web_search = "disabled"')
        lines.append('model_provider = "harness_recording_proxy"')
        lines.append("")
        lines.append("[model_providers.harness_recording_proxy]")
        lines.append('name = "harness recording proxy"')
        lines.append(f'base_url = "{proxy_base_url}/v1"')
        lines.append('wire_api = "responses"')
        auth_env_var = auth_env_var_override or self.api_key_env_var
        if auth_env_var:
            lines.append(f'env_key = "{auth_env_var}"')
        lines.append("")
        lines.append("[history]")
        lines.append('persistence = "none"')
        if self.mcp_servers:
            for server in self.mcp_servers:
                lines.append("")
                lines.append(f"[mcp_servers.{server}]")
        return "\n".join(lines) + "\n"

    def fingerprint(self) -> str:
        """Port-independent sha256 of the frozen combination.

        The per-run proxy port is ephemeral, so the config is fingerprinted
        with the base URL held as a placeholder. The auth env var name is
        represented only as presence/absence in the hash material; the actual
        generated config still carries the configured name, but CodeQL treats
        that string as sensitive credential material and the fingerprint is a
        reproducibility digest, not password storage.
        """
        config_template = self.render_config(
            "{PROXY_BASE_URL}",
            auth_env_var_override=(
                "{AUTH_ENV_VAR}" if self.api_key_env_var is not None else None
            ),
        )
        material = {
            # v3: +egress_guard (the black-hole address is a security boundary
            # for proxy-honoring clients, so a change to it must move the
            # fingerprint). v2 added context_window_tokens; config template
            # validated against codex-cli 0.142.4.
            "schema_version": 3,
            "pinned_cli_version": self.pinned_cli_version,
            "cli_version_command": list(self.cli_version_command),
            "model_id": self.model_id,
            "reasoning_effort": self.reasoning_effort,
            "context_window_tokens": self.context_window_tokens,
            "web_search_enabled": self.web_search_enabled,
            "mcp_servers": sorted(self.mcp_servers),
            "env_allowlist": sorted(self.env_allowlist),
            "env_allowlist_values": {
                name: os.environ.get(name) for name in sorted(self.env_allowlist)
            },
            "api_key_env_var_configured": self.api_key_env_var is not None,
            "cache_policy": self.cache_policy,
            "home_dir_env_var": self.home_dir_env_var,
            "home_env_policy": "HOME is set to the fresh per-run home directory",
            "config_template": config_template,
            # Port-independent shape of the egress guard (values, not the
            # ephemeral proxy host), so tampering with the black-hole address
            # is a fingerprint change.
            "egress_guard": egress_guard_env("http://{PROXY_HOST}"),
        }
        canonical = json.dumps(material, sort_keys=True)
        return _sha256_hex(canonical.encode())


def egress_guard_env(proxy_base_url: str) -> dict[str, str]:
    """Proxy-variable guard: loopback (the recording proxy) is the only
    reachable HTTP destination for proxy-honoring clients."""
    host = urlparse(proxy_base_url).hostname or "127.0.0.1"
    no_proxy = ",".join(sorted({host, "127.0.0.1", "localhost"}))
    guard = {
        "HTTP_PROXY": _BLACKHOLE_PROXY,
        "HTTPS_PROXY": _BLACKHOLE_PROXY,
        "ALL_PROXY": _BLACKHOLE_PROXY,
        "NO_PROXY": no_proxy,
    }
    guard.update({k.lower(): v for k, v in guard.items()})
    return guard


def capture_cli_version(command: list[str], timeout: float = 30.0) -> str:
    """Run the version command and return its stripped stdout."""
    try:
        result = subprocess.run(
            command, capture_output=True, text=True, timeout=timeout, check=False
        )
    except (OSError, subprocess.TimeoutExpired) as exc:
        raise FreezeViolation(f"cannot capture CLI version via {command}: {exc}")
    output = (result.stdout or result.stderr).strip()
    if result.returncode != 0 or not output:
        raise FreezeViolation(
            f"version command {command} failed (exit {result.returncode}): {output!r}"
        )
    return output


@dataclass
class FrozenEnvironment:
    """One run's built, verified environment."""

    config: FreezeConfig
    run_dir: Path  # per-run report dir; codex_home is created under it
    proxy_base_url: str
    cli_version: str | None = None
    home_dir: Path = field(init=False)
    _built: bool = field(default=False, init=False)

    def __post_init__(self) -> None:
        self.run_dir = Path(self.run_dir)
        self.home_dir = self.run_dir / "codex_home"

    # -- lifecycle ---------------------------------------------------------

    def build(self) -> "FrozenEnvironment":
        """Verify the pin, create the fresh home, write the config."""
        self.cli_version = capture_cli_version(self.config.cli_version_command)
        if self.cli_version != self.config.pinned_cli_version:
            raise FreezeViolation(
                "CLI version mismatch: pinned "
                f"{self.config.pinned_cli_version!r}, found {self.cli_version!r} "
                "(no auto-update; re-pin deliberately)"
            )
        if self.home_dir.exists():
            raise FreezeViolation(
                f"per-run home already exists: {self.home_dir} — homes are "
                "fresh per run, never reused"
            )
        self.home_dir.mkdir(parents=True)
        (self.home_dir / "config.toml").write_text(
            self.config.render_config(self.proxy_base_url), encoding="utf-8"
        )
        self._built = True
        return self

    def environment(self) -> dict[str, str]:
        """The complete (not merged) process environment for the agent."""
        if not self._built:
            raise FreezeViolation("environment() before build()")
        env: dict[str, str] = {}
        for name in self.config.env_allowlist:
            if name in os.environ:
                env[name] = os.environ[name]
        if self.config.api_key_env_var and self.config.api_key_env_var in os.environ:
            env[self.config.api_key_env_var] = os.environ[self.config.api_key_env_var]
        env["HOME"] = str(self.home_dir)
        env[self.config.home_dir_env_var] = str(self.home_dir)
        env["OPENAI_BASE_URL"] = f"{self.proxy_base_url}/v1"
        env.update(egress_guard_env(self.proxy_base_url))
        return env

    def fingerprint(self) -> str:
        return self.config.fingerprint()

    def verify_fingerprint(self, registered: str) -> None:
        """Abort if this environment is not the registered one (§8.1.1)."""
        actual = self.fingerprint()
        if actual != registered:
            raise FreezeViolation(
                f"env fingerprint mismatch: registered {registered[:12]}…, "
                f"built {actual[:12]}… — a changed environment is a new "
                "experiment, not a variant"
            )

    def collect_and_destroy(self) -> list[str]:
        """Archive session artifacts, then delete the home (ephemerality).

        Everything the CLI wrote into its home during the run (session logs,
        final config bytes, caches it created despite policy) is moved to
        ``<run_dir>/codex_home_artifacts/`` for the session-parser
        cross-check; the home itself is removed so nothing can leak into a
        later run. Returns the archived paths (home-relative).
        """
        if not self._built:
            raise FreezeViolation("collect_and_destroy() before build()")
        archive = self.run_dir / "codex_home_artifacts"
        archived: list[str] = []
        for path in sorted(p for p in self.home_dir.rglob("*") if p.is_file()):
            rel = path.relative_to(self.home_dir)
            destination = archive / rel
            destination.parent.mkdir(parents=True, exist_ok=True)
            shutil.move(str(path), destination)
            archived.append(rel.as_posix())
        shutil.rmtree(self.home_dir)
        self._built = False
        return archived
