"""Non-billing Codex routing probe (design §10 still-to-confirm #1–#3).

Empirically verifies, against a locally installed Codex CLI build, every
pre-run claim the design leaves "still to confirm" — **without any provider
account, real API key, or network egress**:

1. the frozen config generated by :mod:`harness.runner.env_freeze` parses
   under ``--strict-config`` (key names valid for the pinned build);
2. **all** provider traffic goes to the configured ``base_url`` — the probe's
   scripted upstream is the only reachable HTTP destination (loopback), and
   the run inherits the §8.1.1 egress guard, so a request that ignored the
   override would fail loudly instead of reaching a provider;
3. the recording proxy captures every request byte-exactly and parses
   provider ``usage`` out of the Responses-API SSE stream;
4. the CLI authenticates via the provider ``env_key`` (a fake local marker
   key — never a real credential);
5. ``web_search = "disabled"`` removes the web_search tool from the request;
6. tool execution is **local**: the scripted upstream orders one
   ``exec_command`` (``ls``) and the follow-up request must echo a marker
   file that exists only in the probe workdir (design §10 #2 — the pinned
   build shells out locally; no server-side retrieval path is involved);
7. the CLI writes a session rollout log into its (ephemeral, archived)
   ``CODEX_HOME`` — the session-parser cross-check input exists.

Run::

    python3 -m harness.runner.codex_probe --json probe_verdict.json

Exit code 0 iff every check passes. The scripted upstream never contacts any
external service; the "model" is a two-turn script, so the probe costs zero
tokens and cannot bill anything.
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import sys
import tempfile
import threading
from dataclasses import dataclass, field
from http.server import BaseHTTPRequestHandler, ThreadingHTTPServer
from pathlib import Path

from harness.context_snapshots.proxy import RecordingProxy
from harness.runner.env_freeze import FreezeConfig, FrozenEnvironment, capture_cli_version

PROBE_MARKER = "PROBE_MARKER_1209.txt"
FAKE_API_KEY = "sk-local-probe-not-a-real-key"
DEFAULT_MODEL_ID = "gpt-5.1-codex-max"
DEFAULT_EFFORT = "medium"
DEFAULT_CONTEXT_WINDOW = 272_000


@dataclass
class ScriptedResponsesUpstream:
    """A two-turn scripted Responses-API 'model'.

    Turn 1 orders ``exec_command`` (``ls``); every later turn replies with a
    terminal assistant message. Records every request it sees so the probe
    can assert that nothing bypassed the proxy and that the tool output came
    back from a *local* execution.
    """

    requests: list[dict] = field(default_factory=list)
    _server: ThreadingHTTPServer | None = None

    def start(self) -> str:
        upstream = self

        class Handler(BaseHTTPRequestHandler):
            protocol_version = "HTTP/1.1"

            def log_message(self, *args) -> None:  # silence stderr
                pass

            def do_GET(self) -> None:  # noqa: N802
                upstream._note(self, b"")
                self.send_response(404)
                self.send_header("Content-Length", "0")
                self.end_headers()

            def do_POST(self) -> None:  # noqa: N802
                length = int(self.headers.get("Content-Length") or 0)
                body = self.rfile.read(length) if length else b""
                turn = upstream._note(self, body)
                payload = upstream._sse_for_turn(turn)
                self.send_response(200)
                self.send_header("Content-Type", "text/event-stream")
                self.send_header("Content-Length", str(len(payload)))
                self.send_header("Connection", "close")
                self.end_headers()
                self.wfile.write(payload)

        self._server = ThreadingHTTPServer(("127.0.0.1", 0), Handler)
        threading.Thread(target=self._server.serve_forever, daemon=True).start()
        return f"http://127.0.0.1:{self._server.server_address[1]}"

    def stop(self) -> None:
        if self._server is not None:
            self._server.shutdown()
            self._server.server_close()
            self._server = None

    def _note(self, handler: BaseHTTPRequestHandler, body: bytes) -> int:
        self.requests.append(
            {
                "method": handler.command,
                "path": handler.path,
                "authorization": handler.headers.get("Authorization"),
                "body": body.decode("utf-8", errors="replace"),
            }
        )
        return sum(1 for r in self.requests if r["method"] == "POST")

    def _sse_for_turn(self, turn: int) -> bytes:
        if turn == 1:
            item = {
                "type": "function_call",
                "id": "fc_probe_1",
                "call_id": "call_probe_1",
                "name": "exec_command",
                "arguments": json.dumps({"cmd": "ls"}),
                "status": "completed",
            }
        else:
            item = {
                "type": "message",
                "id": f"msg_probe_{turn}",
                "role": "assistant",
                "status": "completed",
                "content": [{"type": "output_text", "text": "probe complete"}],
            }
        completed = {
            "id": f"resp_probe_{turn}",
            "object": "response",
            "status": "completed",
            "output": [item],
            "usage": {
                "input_tokens": 40 + turn,
                "output_tokens": turn,
                "total_tokens": 40 + 2 * turn,
            },
        }
        events = [
            ("response.created",
             {"type": "response.created",
              "response": {"id": completed["id"], "status": "in_progress"}}),
            ("response.output_item.done",
             {"type": "response.output_item.done", "output_index": 0, "item": item}),
            ("response.completed",
             {"type": "response.completed", "response": completed}),
        ]
        return "".join(
            f"event: {name}\ndata: {json.dumps(data)}\n\n" for name, data in events
        ).encode()


def run_probe(
    *,
    codex_binary: str = "codex",
    model_id: str = DEFAULT_MODEL_ID,
    reasoning_effort: str = DEFAULT_EFFORT,
    context_window_tokens: int = DEFAULT_CONTEXT_WINDOW,
    work_root: str | Path | None = None,
    timeout_seconds: float = 180.0,
) -> dict:
    """Run the probe; returns a JSON-ready verdict dict (see module doc)."""
    root = Path(work_root) if work_root else Path(tempfile.mkdtemp(prefix="codex-probe-"))
    run_dir = root / "run"
    if run_dir.exists():
        shutil.rmtree(run_dir)
    run_dir.mkdir(parents=True)
    workdir = run_dir / "workdir"
    workdir.mkdir()
    (workdir / PROBE_MARKER).write_text("marker for local-exec check\n")

    pinned = capture_cli_version([codex_binary, "--version"])
    upstream = ScriptedResponsesUpstream()
    upstream_url = upstream.start()
    proxy = RecordingProxy(
        upstream_base_url=upstream_url, archive_dir=run_dir, run_id="codex-probe"
    )
    proxy_url = proxy.start()

    os.environ.setdefault("OPENAI_API_KEY", FAKE_API_KEY)
    freeze = FreezeConfig(
        pinned_cli_version=pinned,
        cli_version_command=[codex_binary, "--version"],
        model_id=model_id,
        reasoning_effort=reasoning_effort,
        context_window_tokens=context_window_tokens,
        api_key_env_var="OPENAI_API_KEY",
    )
    frozen = FrozenEnvironment(
        config=freeze, run_dir=run_dir, proxy_base_url=proxy_url
    ).build()

    command = [
        codex_binary, "exec",
        "--cd", str(workdir),
        "--skip-git-repo-check",
        "--strict-config",
        "--sandbox", "read-only",
        "Run the ls command once, then finish.",
    ]
    timed_out = False
    try:
        completed = subprocess.run(
            command,
            env=frozen.environment(),
            capture_output=True,
            text=True,
            timeout=timeout_seconds,
            stdin=subprocess.DEVNULL,
        )
        exit_code: int | None = completed.returncode
        stderr = completed.stderr
    except subprocess.TimeoutExpired as exc:
        timed_out = True
        exit_code = None
        stderr = (exc.stderr or b"").decode(errors="replace") if exc.stderr else ""
    finally:
        proxy.stop()
        upstream.stop()
        archived = frozen.collect_and_destroy()

    posts = [r for r in upstream.requests if r["method"] == "POST"]
    non_posts = [r for r in upstream.requests if r["method"] != "POST"]
    first_body = json.loads(posts[0]["body"]) if posts else {}
    tools = first_body.get("tools", [])
    tool_names = [t.get("name") or t.get("type") for t in tools]

    checks = {
        "config_strict_ok": "config.toml" not in (stderr or ""),
        "agent_exit_zero": exit_code == 0,
        "routed_all_traffic": (
            len(posts) >= 2
            and all(r["path"] == "/v1/responses" for r in posts)
            and len(proxy.records) == len(posts)
        ),
        "proxy_byte_exact": all(
            (run_dir / r.payload_path).read_bytes()
            == posts[i]["body"].encode("utf-8")
            for i, r in enumerate(sorted(proxy.records, key=lambda r: r.ordinal))
        ) if posts and len(proxy.records) == len(posts) else False,
        "usage_captured": all(
            r.input_tokens is not None for r in proxy.records
        ) and bool(proxy.records),
        "auth_via_env_key": all(
            (r["authorization"] or "").endswith(os.environ["OPENAI_API_KEY"])
            for r in posts
        ) and bool(posts),
        "web_search_tool_absent": "web_search" not in tool_names,
        "local_exec_observed": (
            len(posts) >= 2 and PROBE_MARKER in posts[1]["body"]
        ),
        "session_log_archived": any("sessions/" in a and a.endswith(".jsonl")
                                    for a in archived),
        "no_stray_requests": not non_posts,
        "not_timed_out": not timed_out,
    }
    return {
        "pinned_cli_version": pinned,
        "model_id": model_id,
        "reasoning_effort": reasoning_effort,
        "context_window_tokens": context_window_tokens,
        "command": command,
        "checks": checks,
        "confirmed": all(checks.values()),
        "posts_seen": [{"path": r["path"]} for r in posts],
        "tool_names_offered": tool_names,
        "proxy_records": len(proxy.records),
        "stderr_tail": (stderr or "")[-1500:],
        "run_dir": str(run_dir),
    }


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--codex-binary", default="codex")
    parser.add_argument("--model", default=DEFAULT_MODEL_ID)
    parser.add_argument("--effort", default=DEFAULT_EFFORT)
    parser.add_argument("--context-window", type=int, default=DEFAULT_CONTEXT_WINDOW)
    parser.add_argument("--json", type=Path, default=None,
                        help="write the full verdict JSON here")
    args = parser.parse_args(argv)

    verdict = run_probe(
        codex_binary=args.codex_binary,
        model_id=args.model,
        reasoning_effort=args.effort,
        context_window_tokens=args.context_window,
    )
    if args.json:
        args.json.write_text(json.dumps(verdict, indent=2) + "\n", encoding="utf-8")
    print(f"pinned CLI : {verdict['pinned_cli_version']}")
    for name, passed in verdict["checks"].items():
        print(f"  {'PASS' if passed else 'FAIL'}  {name}")
    print("CONFIRMED" if verdict["confirmed"] else "FAILED")
    return 0 if verdict["confirmed"] else 1


if __name__ == "__main__":
    sys.exit(main())
